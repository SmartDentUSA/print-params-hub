const ORDER: KbTab[] = ['parametros', 'catalogo', 'videos', 'artigos', 'distribuidores', 'eventos'];

interface Props {
  active: KbTab;
  onChange: (t: KbTab) => void;
}

export default function KbTabSwitcher({ active, onChange }: Props) {
  const { t: tr } = useLanguage();
  return (
    <div className="kb-switcher-wrap">
      <div className="kb-switcher" role="tablist">
        {ORDER.map((t) => (
          <button
            key={t}
            role="tab"
            aria-pressed={active === t}
            className={`kb-tab-btn${active === t ? ' on' : ''}`}
            onClick={() => onChange(t)}
          >
            {ICONS[t]}
            <span>{tr(`kb.tabs.${t}`)}</span>
          </button>
        ))}
      </div>
    </div>
  );
}